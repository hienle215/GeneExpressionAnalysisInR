###Hands on GSEA-based pathway analysis
Hands on GSEA-based pathway analysis

Now let's do a GSEA analysis. First, download the GSEA.rnk file (you'll have to unzip it). This file has two columns: one for gene id, and the other for gene rank produced using signal2noise method that was discussed in lecture. Go to http://www.webgestalt.org/ and set the following parameters:
  
  - Organism: Homo sapiens

- Method: GSEA

- Functional database: pathway --> KEGG

Then upload the rank file and click submit. Wait for a little and answer the quiz questions.
